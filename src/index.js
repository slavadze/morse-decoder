function decode(expr) {

    
    let size = 10;
    let arrayTen = [];
    for (let i = 0; i < expr.length; i+= size){
        arrayTen.push(expr.slice(i, i + size));
    }

    repl = arrayTen.map(j=>{
        return j.replace(/00/g, '').replace(/11/g, '-').replace(/10/g, '.')
    })


    return repl;
}

